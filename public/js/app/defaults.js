// Configure the default values.

;(function() {

  // globals
  qd.defaults.mode = 'drag';
  qd.defaults.zoom = 1;
  qd.defaults.size = 3200;

  // pen
  qd.defaults.pen.mode       = 'draw';
  qd.defaults.pen.color      = '#000';
  qd.defaults.pen.size       = 10;
  qd.defaults.pen.eraserSize = 10;
  qd.defaults.pen.opacity    = 1;

})();